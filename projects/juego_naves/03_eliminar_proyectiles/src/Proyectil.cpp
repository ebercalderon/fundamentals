#include "Proyectil.h"

///////////////////////////////////////////////////////////////////////////
Proyectil::Proyectil(int x, int y)
{
  m_x = x;
  m_y = y;
}

///////////////////////////////////////////////////////////////////////////
void Proyectil::update()
{
  if (m_y >= 1) m_y--;
}

///////////////////////////////////////////////////////////////////////////
void Proyectil::draw()
{
  mvaddch(m_y, m_x, '^');
}

///////////////////////////////////////////////////////////////////////
bool Proyectil::limite()
{
  // Si el proyectil llegó al límite devuelve TRUE sino FALSE.
  if( m_y < 1 ) return true;
  return false;
}

///////////////////////////////////////////////////////////////////////
int Proyectil::getX() { return m_x; }
int Proyectil::getY() { return m_y; }
